const char* status_to_string(LONG status)
{
	int idx = 0;

	while (smb2_error_map_table[idx].status_string != NULL) {
		if ((smb2_error_map_table[idx].smb2_status) == status) {
			return(smb2_error_map_table[idx].status_string);
		}
		idx++;
	}
	return(NULL);
}
